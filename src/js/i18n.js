  function getLang() {
    const saved = localStorage.getItem(STORAGE_KEY);
    return dictionary[saved] ? saved : defaultLang;
  }

  function t(key, params = {}) {
    const lang = getLang();
    const template = dictionary[lang][key] || dictionary[defaultLang][key] || key;
    return Object.entries(params).reduce((text, [name, value]) => {
      return text.replaceAll(`{${name}}`, String(value));
    }, template);
  }

  function apply(root = document) {
    root.querySelectorAll("[data-i18n]").forEach((node) => {
      node.textContent = t(node.dataset.i18n);
    });
    root.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
      node.setAttribute("placeholder", t(node.dataset.i18nPlaceholder));
    });
    root.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
      node.setAttribute("aria-label", t(node.dataset.i18nAriaLabel));
    });
    root.querySelectorAll("[data-i18n-alt]").forEach((node) => {
      node.setAttribute("alt", t(node.dataset.i18nAlt));
    });
    root.querySelectorAll("[data-i18n-title]").forEach((node) => {
      node.setAttribute("title", t(node.dataset.i18nTitle));
    });
    root.querySelectorAll("[data-lang-switch]").forEach((node) => {
      node.value = getLang();
    });
    document.documentElement.lang = getLang() === "zh" ? "zh-Hant" : "en";
    const page = document.body?.dataset.page;
    document.title = t(page === "admin" ? "document.adminTitle" : "document.studentTitle");
  }

  function setLang(lang) {
    const nextLang = dictionary[lang] ? lang : defaultLang;
    localStorage.setItem(STORAGE_KEY, nextLang);
    apply();
    window.dispatchEvent(new CustomEvent("tgga-lang-change", { detail: nextLang }));
  }

  function bind() {
    document.addEventListener("change", (event) => {
      const switcher = event.target.closest("[data-lang-switch]");
      if (switcher) setLang(switcher.value);
    });
    window.addEventListener("storage", (event) => {
      if (event.key === STORAGE_KEY) {
        apply();
        window.dispatchEvent(new CustomEvent("tgga-lang-change", { detail: getLang() }));
      }
    });
    apply();
  }

  window.GoldReserveI18n = {
    getLang,
    setLang,
    apply,
    t
  };

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", bind);
  } else {
    bind();
  }
